#ifndef EXPERIMENTS_RANDOMUTILS_H
#define EXPERIMENTS_RANDOMUTILS_H

#include <random>
using namespace std;

class RandomUtils {
public:
    /**
     * Gets a random number used as a master seed. Such seeds are not expected to be reproducible
     * anywhere else.
     * @return the master seed
     */
    static unsigned int generateMasterSeed();

    /**
     * Creates the static random number generator with the given seed.
     * From this point onwards all the random numbers generated by this class
     * will be drawn from a distribution using such generator.
     * @param seed seed to initialize the PRNG
     */
    static void setMasterSeed(unsigned int seed);

    /**
     * Generates a random number drawn from a uniform 0-1 distribution.
     * @return the random number.
     */
    static double U();

    /**
     * Generates a random number that is not a result from setting a seed.
     * There are no seedable/reproducibility guarantees.
     * @return a random number
     */
    static unsigned int getUnseededRandomNumber();
};

#endif //EXPERIMENTS_RANDOMUTILS_H
